/// Retorna o país pelo código ISO (case insensitive)
CountryItem? countryByCode(String? code) {
  if (code == null || code.isEmpty) return null;
  final upper = code.toUpperCase();
  try {
    return kCountries.firstWhere((c) => c.code == upper);
  } catch (_) {
    return null;
  }
}

/// Tenta mapear uma string livre (nome, código, adjetivo em PT) para um CountryItem
CountryItem? countryByAny(String? value) {
  if (value == null || value.isEmpty) return null;
  final upper = value.toUpperCase().trim();

  // 1. Código ISO direto (2 letras)
  if (upper.length == 2) {
    final byCode = countryByCode(upper);
    if (byCode != null) return byCode;
  }

  // 2. Nome em português (match exato ou parcial)
  for (final c in kCountries) {
    if (c.name.toUpperCase() == upper) return c;
  }
  for (final c in kCountries) {
    if (c.name.toUpperCase().contains(upper) || upper.contains(c.name.toUpperCase())) return c;
  }

  // 3. Adjetivos comuns em PT que o OCR pode retornar
  const Map<String, String> adjToCode = {
    'AMERICANO': 'US', 'AMERICANA': 'US',
